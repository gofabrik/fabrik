// New returns directive instances for one generation run.
func New() []gen.Directive {
	group := routerdir.NewGroup()
	routes := routerdir.NewRouteTable()
	mw := routerdir.NewMiddleware()
	host := routerdir.NewHost(group, routes, mw)
	tpl := tpldir.NewTemplates()
	cfg := configdir.New()
	assetsConfig := assetOptionsSource{cfg: cfg}
	provider := core.NewProvider(cfg)
	jobsJob, jobsCron := jobsdir.New()
	cliCmd, cliFlag, cliArg, cliExample, cliGroup, cliRoot, cliMW := clidir.New()
	// Registry order is presentation only; lifecycle order comes from Meta.Tier.
	return []gen.Directive{
		provider,
		core.NewSelect(provider, cfg),
		core.NewHook(cfg),
		routerdir.NewHTTP(host),
		routerdir.NewHandle(host),
		routerdir.NewStatic(host),
		group,
		mw,
		routerdir.NewNotFound(host),
		routerdir.NewMethodNotAllowed(host),
		webdir.NewWeb(host),
		tpl,
		tpldir.NewFuncs(tpl),
		assetsdir.NewAssets(host, tpl, assetsConfig),
		migdir.NewMigrations(),
		jobsJob,
		jobsCron,
		cliCmd,
		cliFlag,
		cliArg,
		cliExample,
		cliGroup,
		cliRoot,
		cliMW,
		cfg,
	}
}

// DirectivesDoc renders the directive reference from registry metadata.
func DirectivesDoc() string {
	var b strings.Builder
	b.WriteString("# Directives\n")
	b.WriteString("\nGenerated by `fabrik directives`. Do not edit.\n")
	b.WriteString("\nEvery directive has the shape `//fabrik:NAME positional-args key=value-options`.\n")
	b.WriteString("Positional arguments come first, `key=value` options after; values may be\n")
	b.WriteString("double-quoted to contain spaces. Unknown option keys are errors, which is what\n")
	b.WriteString("makes new keys backwards-compatible additions.\n")

	directives := New()
	sort.Slice(directives, func(i, j int) bool { return directives[i].Name() < directives[j].Name() })
	for _, d := range directives {
		m := d.Meta()
		if m.Hidden {
			continue
		}
		fmt.Fprintf(&b, "\n## fabrik:%s\n\n", d.Name())
		fmt.Fprintf(&b, "%s\n", m.Doc)
		if len(m.Pos) > 0 {
			b.WriteString("\nPositional arguments:\n\n")
			for _, p := range m.Pos {
				fmt.Fprintf(&b, "- `%s`%s\n", p.Name, valuesNote(p.Kind, p.Values))
			}
		}
		if len(m.Attrs) > 0 {
			b.WriteString("\nOptions:\n\n")
			for _, a := range m.Attrs {
				req := ""
				if a.Required {
					req = " (required)"
				}
				fmt.Fprintf(&b, "- `%s=`%s%s\n", a.Key, valuesNote(a.Kind, a.Values), req)
			}
		}
	}
	return b.String()
}

func valuesNote(kind gen.ValueKind, values []string) string {
	if len(values) == 0 {
		return ""
	}
	if kind == gen.KindEnum {
		return " - one of " + strings.Join(values, ", ")
	}
	return " - e.g. " + strings.Join(values, ", ")
}

type assetOptionsSource struct {
	cfg *configdir.Config
}

const assetOptionsPtr = "*github.com/gofabrik/fabrik/assetmapper.Options"

func (r assetOptionsSource) Node() (string, token.Position, bool) {
	nd := r.cfg.NodeByType(assetOptionsPtr)
	if nd == nil {
		return "", token.Position{}, false
	}
	return nd.Section(), nd.Pos(), true
}

func (r assetOptionsSource) Load(g *gen.Gen) (string, diag.Diagnostics, bool) {
	nd := r.cfg.NodeByType(assetOptionsPtr)
	// Reuse the flow's lazy binding when application code also injects the config.
	return g.Instance(types.NewPointer(nd.Named()), "")
}
